import React, { useState, useCallback, useRef, useEffect } from 'react';
import { Stage, Layer } from 'react-konva';
import { Scene, CommandHistory } from '@canvas-kit/core';
import type { DrawingObject } from '@canvas-kit/core';
import { KonvaDesigner } from './KonvaDesigner';
import { FreeDrawingCanvas } from './FreeDrawingCanvas';
import { EditableText } from './EditableText';

export type DesignerTool = 'select' | 'draw' | 'text' | 'rect' | 'circle';

export interface DrawingTool {
    mode: 'brush' | 'eraser';
    color: string;
    width: number;
}

export interface TextObject {
    id: string;
    x: number;
    y: number;
    text: string;
    fontSize: number;
    fontFamily: string;
    fill: string;
    width: number;
}

export interface AdvancedDesignerProps {
    width: number;
    height: number;
    scene: Scene;
    onSceneChange?: (scene: Scene) => void;
    onSelectionChange?: (selectedObjects: DrawingObject[]) => void;
    commandHistory?: CommandHistory;
    showToolbar?: boolean;
    enableKeyboardShortcuts?: boolean;
    enableUndoRedo?: boolean;
}

export const AdvancedDesigner: React.FC<AdvancedDesignerProps> = ({
    width,
    height,
    scene,
    onSceneChange,
    onSelectionChange,
    commandHistory = new CommandHistory(),
    showToolbar = true,
    enableKeyboardShortcuts = true,
    enableUndoRedo = true
}) => {
    const [currentTool, setCurrentTool] = useState<DesignerTool>('select');
    const [drawingTool, setDrawingTool] = useState<DrawingTool>({
        mode: 'brush',
        color: '#000000',
        width: 5
    });
    const [textObjects, setTextObjects] = useState<TextObject[]>([]);
    const [selectedTextId, setSelectedTextId] = useState<string | null>(null);

    const stageRef = useRef<any>(null);

    // ÌÇ§Î≥¥Îìú Îã®Ï∂ïÌÇ§ Ï≤òÎ¶¨
    useEffect(() => {
        if (!enableKeyboardShortcuts) return;

        const handleKeyDown = (e: KeyboardEvent) => {
            // ÏûÖÎ†• ÌïÑÎìúÏóêÏÑúÎäî Îã®Ï∂ïÌÇ§ ÎπÑÌôúÏÑ±Ìôî
            if (e.target instanceof HTMLInputElement || e.target instanceof HTMLTextAreaElement) {
                return;
            }

            const isCtrl = e.ctrlKey || e.metaKey;
            const isShift = e.shiftKey;

            if (enableUndoRedo && isCtrl) {
                if (e.key === 'z' && !isShift) {
                    e.preventDefault();
                    if (commandHistory.canUndo()) {
                        commandHistory.undo();
                        console.log('Undo executed via keyboard shortcut');
                    }
                } else if ((e.key === 'y') || (e.key === 'z' && isShift)) {
                    e.preventDefault();
                    if (commandHistory.canRedo()) {
                        commandHistory.redo();
                        console.log('Redo executed via keyboard shortcut');
                    }
                }
            }

            // ÎèÑÍµ¨ Ï†ÑÌôò Îã®Ï∂ïÌÇ§
            if (!isCtrl && !isShift) {
                switch (e.key) {
                    case '1':
                        e.preventDefault();
                        setCurrentTool('select');
                        break;
                    case '2':
                        e.preventDefault();
                        setCurrentTool('draw');
                        break;
                    case '3':
                        e.preventDefault();
                        setCurrentTool('text');
                        break;
                    case '4':
                        e.preventDefault();
                        setCurrentTool('rect');
                        break;
                    case '5':
                        e.preventDefault();
                        setCurrentTool('circle');
                        break;
                    case 'Escape':
                        e.preventDefault();
                        setCurrentTool('select');
                        break;
                }
            }
        };

        window.addEventListener('keydown', handleKeyDown);
        return () => window.removeEventListener('keydown', handleKeyDown);
    }, [enableKeyboardShortcuts, enableUndoRedo, commandHistory]);

    // ÎîîÎ≤ÑÍπÖÏùÑ ÏúÑÌïú command history ÏÉÅÌÉú Ï∂îÏ†Å
    useEffect(() => {
        const handleHistoryChange = () => {
            console.log('Command history changed:', {
                canUndo: commandHistory.canUndo(),
                canRedo: commandHistory.canRedo()
            });
        };

        // CommandHistoryÏóê Ïù¥Î≤§Ìä∏ Î¶¨Ïä§ÎÑàÍ∞Ä ÏûàÎã§Î©¥ Ï∂îÍ∞Ä
        // ÌòÑÏû¨Îäî ÏàòÎèôÏúºÎ°ú ÏÉÅÌÉú Î≥ÄÍ≤ΩÏùÑ Í∞êÏßÄÌïòÍ∏∞ ÏúÑÌï¥ interval ÏÇ¨Ïö©
        const interval = setInterval(handleHistoryChange, 1000);

        return () => clearInterval(interval);
    }, [commandHistory]);

    const handleToolChange = useCallback((tool: DesignerTool) => {
        setCurrentTool(tool);
        if (tool !== 'text') {
            setSelectedTextId(null);
        }
    }, []);

    const handleDrawingToolChange = useCallback((tool: DrawingTool) => {
        setDrawingTool(tool);
    }, []);

    // ÌÖçÏä§Ìä∏ Í¥ÄÎ†® Ìï∏Îì§Îü¨Îì§
    const handleTextChange = useCallback((id: string, newText: string) => {
        setTextObjects(prev => prev.map(text =>
            text.id === id ? { ...text, text: newText } : text
        ));
    }, []);

    const handleTextSelect = useCallback((id: string) => {
        setSelectedTextId(selectedTextId === id ? null : id);
    }, [selectedTextId]);

    const handleTextTransform = useCallback((id: string, attrs: any) => {
        setTextObjects(prev => prev.map(text =>
            text.id === id ? { ...text, ...attrs } : text
        ));
    }, []);

    const addTextAtPosition = useCallback((x: number, y: number) => {
        const newText: TextObject = {
            id: `text-${Date.now()}`,
            x,
            y,
            text: 'New text',
            fontSize: 20,
            fontFamily: 'Arial',
            fill: '#000000',
            width: 200
        };

        setTextObjects(prev => [...prev, newText]);
        setSelectedTextId(newText.id);
    }, []);

    // Ïä§ÌÖåÏù¥ÏßÄ ÌÅ¥Î¶≠ Ìï∏Îì§Îü¨
    const handleStageClick = useCallback((e: any) => {
        if (currentTool === 'text') {
            const pos = e.target.getStage().getPointerPosition();
            if (pos) {
                addTextAtPosition(pos.x, pos.y);
            }
        }
    }, [currentTool, addTextAtPosition]);

    const tools = [
        { id: 'select', label: 'ÏÑ†ÌÉù', icon: '‚ÜñÔ∏è', shortcut: '1' },
        { id: 'draw', label: 'Í∑∏Î¶¨Í∏∞', icon: '‚úèÔ∏è', shortcut: '2' },
        { id: 'text', label: 'ÌÖçÏä§Ìä∏', icon: 'üìù', shortcut: '3' },
        { id: 'rect', label: 'ÏÇ¨Í∞ÅÌòï', icon: '‚¨ú', shortcut: '4' },
        { id: 'circle', label: 'Ïõê', icon: '‚≠ï', shortcut: '5' }
    ];

    const brushColors = [
        '#000000', '#ff0000', '#00ff00', '#0000ff', '#ffff00',
        '#ff00ff', '#00ffff', '#ff8800', '#8800ff', '#888888'
    ];

    return (
        <div className="flex flex-col h-full">
            {showToolbar && (
                <div className="bg-gray-100 border-b border-gray-300 p-3">
                    <div className="flex items-center gap-4">
                        {/* Ìà¥ ÏÑ†ÌÉù */}
                        <div className="flex gap-1">
                            {tools.map(tool => (
                                <button
                                    key={tool.id}
                                    onClick={() => handleToolChange(tool.id as DesignerTool)}
                                    className={`px-3 py-2 rounded transition-colors ${currentTool === tool.id
                                        ? 'bg-blue-500 text-white'
                                        : 'bg-white hover:bg-gray-50 border border-gray-300'
                                        }`}
                                    title={`${tool.label} ${enableKeyboardShortcuts ? `(${tool.shortcut})` : ''}`}
                                >
                                    <span className="mr-1">{tool.icon}</span>
                                    {tool.label}
                                    {enableKeyboardShortcuts && (
                                        <kbd className="ml-2 px-1 py-0.5 text-xs bg-gray-200 text-gray-600 rounded border">
                                            {tool.shortcut}
                                        </kbd>
                                    )}
                                </button>
                            ))}
                        </div>

                        {/* Í∑∏Î¶¨Í∏∞ ÎèÑÍµ¨ ÏÑ§Ï†ï */}
                        {currentTool === 'draw' && (
                            <>
                                <div className="h-6 w-px bg-gray-300" />

                                {/* Î∏åÎü¨Ïãú/ÏßÄÏö∞Í∞ú */}
                                <div className="flex gap-1">
                                    <button
                                        onClick={() => handleDrawingToolChange({ ...drawingTool, mode: 'brush' })}
                                        className={`px-2 py-1 rounded text-sm ${drawingTool.mode === 'brush'
                                            ? 'bg-blue-500 text-white'
                                            : 'bg-white border border-gray-300'
                                            }`}
                                    >
                                        üñåÔ∏è Î∏åÎü¨Ïãú
                                    </button>
                                    <button
                                        onClick={() => handleDrawingToolChange({ ...drawingTool, mode: 'eraser' })}
                                        className={`px-2 py-1 rounded text-sm ${drawingTool.mode === 'eraser'
                                            ? 'bg-blue-500 text-white'
                                            : 'bg-white border border-gray-300'
                                            }`}
                                    >
                                        üßπ ÏßÄÏö∞Í∞ú
                                    </button>
                                </div>

                                {/* ÏÉâÏÉÅ ÌåîÎ†àÌä∏ */}
                                {drawingTool.mode === 'brush' && (
                                    <div className="flex gap-1">
                                        {brushColors.map(color => (
                                            <button
                                                key={color}
                                                onClick={() => handleDrawingToolChange({ ...drawingTool, color })}
                                                className={`w-6 h-6 rounded border-2 ${drawingTool.color === color
                                                    ? 'border-gray-800'
                                                    : 'border-gray-300'
                                                    }`}
                                                style={{ backgroundColor: color }}
                                            />
                                        ))}
                                    </div>
                                )}

                                {/* ÎëêÍªò Ï°∞Ï†à */}
                                <div className="flex items-center gap-2">
                                    <span className="text-sm text-gray-600">ÎëêÍªò:</span>
                                    <input
                                        type="range"
                                        min="1"
                                        max="20"
                                        value={drawingTool.width}
                                        onChange={(e) => handleDrawingToolChange({
                                            ...drawingTool,
                                            width: parseInt(e.target.value)
                                        })}
                                        className="w-20"
                                    />
                                    <span className="text-sm text-gray-600 w-6">{drawingTool.width}</span>
                                </div>
                            </>
                        )}

                        {/* Ïã§Ìñâ Ï∑®ÏÜå/Îã§Ïãú Ïã§Ìñâ */}
                        {enableUndoRedo && (
                            <>
                                <div className="h-6 w-px bg-gray-300" />
                                <div className="flex gap-1">
                                    <button
                                        onClick={() => {
                                            commandHistory.undo();
                                            console.log('Undo executed via toolbar button');
                                        }}
                                        disabled={!commandHistory.canUndo()}
                                        className="px-3 py-1 rounded text-sm bg-white border border-gray-300 disabled:opacity-50 disabled:cursor-not-allowed hover:bg-gray-50 transition-colors"
                                        title={`Ïã§Ìñâ Ï∑®ÏÜå ${enableKeyboardShortcuts ? '(Ctrl+Z)' : ''}`}
                                    >
                                        ‚Ü∂ Ïã§Ìñâ Ï∑®ÏÜå
                                    </button>
                                    <button
                                        onClick={() => {
                                            commandHistory.redo();
                                            console.log('Redo executed via toolbar button');
                                        }}
                                        disabled={!commandHistory.canRedo()}
                                        className="px-3 py-1 rounded text-sm bg-white border border-gray-300 disabled:opacity-50 disabled:cursor-not-allowed hover:bg-gray-50 transition-colors"
                                        title={`Îã§Ïãú Ïã§Ìñâ ${enableKeyboardShortcuts ? '(Ctrl+Y)' : ''}`}
                                    >
                                        ‚Ü∑ Îã§Ïãú Ïã§Ìñâ
                                    </button>
                                </div>
                            </>
                        )}
                    </div>
                </div>
            )}

            <div className="flex-1 relative">
                {/* Í∏∞Î≥∏ ÎîîÏûêÏù¥ÎÑà (ÏÑ†ÌÉù, ÎèÑÌòï Îì±) */}
                {currentTool === 'select' && (
                    <KonvaDesigner
                        width={width}
                        height={height}
                        scene={scene}
                        onSceneChange={onSceneChange}
                        onSelectionChange={onSelectionChange}
                        enableKeyboardShortcuts={enableKeyboardShortcuts}
                        enableTextEditing={true}
                        enableMultiSelect={true}
                        enableRectangleSelection={true}
                    />
                )}

                {/* ÏûêÏú† Í∑∏Î¶¨Í∏∞ Î∞è ÌÖçÏä§Ìä∏Î•º ÏúÑÌïú Î≥ÑÎèÑ Stage */}
                {(currentTool === 'draw' || currentTool === 'text') && (
                    <Stage
                        width={width}
                        height={height}
                        ref={stageRef}
                        onClick={handleStageClick}
                    >
                        <Layer>
                            {/* ÏûêÏú† Í∑∏Î¶¨Í∏∞ */}
                            {currentTool === 'draw' && (
                                <FreeDrawingCanvas
                                    width={width}
                                    height={height}
                                    tool={drawingTool}
                                    stage={stageRef.current || undefined}
                                />
                            )}

                            {/* ÌÖçÏä§Ìä∏ Í∞ùÏ≤¥Îì§ */}
                            {currentTool === 'text' && textObjects.map(text => (
                                <EditableText
                                    key={text.id}
                                    x={text.x}
                                    y={text.y}
                                    text={text.text}
                                    fontSize={text.fontSize}
                                    fontFamily={text.fontFamily}
                                    fill={text.fill}
                                    width={text.width}
                                    isSelected={selectedTextId === text.id}
                                    onTextChange={(newText) => handleTextChange(text.id, newText)}
                                    onSelect={() => handleTextSelect(text.id)}
                                    onTransform={(attrs) => handleTextTransform(text.id, attrs)}
                                />
                            ))}
                        </Layer>
                    </Stage>
                )}

                {/* Ìà¥ ÏïàÎÇ¥ */}
                {currentTool === 'text' && (
                    <div className="absolute bottom-4 left-4 bg-blue-100 border border-blue-300 rounded p-2 text-sm">
                        üí° Ï∫îÎ≤ÑÏä§Î•º ÌÅ¥Î¶≠ÌïòÏó¨ ÌÖçÏä§Ìä∏Î•º Ï∂îÍ∞ÄÌïòÏÑ∏Ïöî
                    </div>
                )}

                {/* ÌÇ§Î≥¥Îìú Îã®Ï∂ïÌÇ§ ÏïàÎÇ¥ */}
                {enableKeyboardShortcuts && (
                    <div className="absolute bottom-4 right-4 bg-gray-100 border border-gray-300 rounded p-3 text-xs max-w-xs">
                        <h4 className="font-medium mb-2">‚å®Ô∏è ÌÇ§Î≥¥Îìú Îã®Ï∂ïÌÇ§</h4>
                        <div className="space-y-1">
                            {enableUndoRedo && (
                                <>
                                    <div><kbd className="px-1 py-0.5 bg-white rounded border text-xs">Ctrl+Z</kbd> Ïã§Ìñâ Ï∑®ÏÜå</div>
                                    <div><kbd className="px-1 py-0.5 bg-white rounded border text-xs">Ctrl+Y</kbd> Îã§Ïãú Ïã§Ìñâ</div>
                                </>
                            )}
                            <div><kbd className="px-1 py-0.5 bg-white rounded border text-xs">1</kbd> ÏÑ†ÌÉù ÎèÑÍµ¨</div>
                            <div><kbd className="px-1 py-0.5 bg-white rounded border text-xs">2</kbd> Í∑∏Î¶¨Í∏∞ ÎèÑÍµ¨</div>
                            <div><kbd className="px-1 py-0.5 bg-white rounded border text-xs">3</kbd> ÌÖçÏä§Ìä∏ ÎèÑÍµ¨</div>
                            <div><kbd className="px-1 py-0.5 bg-white rounded border text-xs">4</kbd> ÏÇ¨Í∞ÅÌòï ÎèÑÍµ¨</div>
                            <div><kbd className="px-1 py-0.5 bg-white rounded border text-xs">5</kbd> Ïõê ÎèÑÍµ¨</div>
                            <div><kbd className="px-1 py-0.5 bg-white rounded border text-xs">Esc</kbd> ÏÑ†ÌÉù ÎèÑÍµ¨Î°ú Î≥µÍ∑Ä</div>
                        </div>
                    </div>
                )}
            </div>
        </div>
    );
};
